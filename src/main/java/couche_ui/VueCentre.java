    private void jButtonCréerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCréerActionPerformed
        model.setRowCount(0);//vide la jTable
        infoSaisies();           
        if(!nomCentreSaisi.equals("")){
            if(nomEtablissementSelected.equals("---")) JOptionPane.showMessageDialog(this, " Veuillez sélectionner un établissement svp !");
            else{
                for(Arrondissement a:arrondissements){
                    if(a.getNomarr().equals(nomArrondissementSelected)){
                        arrondissementSelected=a;
                        break;
                    }
                }
                for(Etablissement e:etablissements){
                    if(e.getNometab().equals(nomEtablissementSelected)){
                        etablissementSelected=e;
                        break;
                    }
                }
                try{
                    metier.creerCentre(new Centre(nomCentreSaisi.toUpperCase(),arrondissementSelected,etablissementSelected));
                    JOptionPane.showMessageDialog(this, " Centre créé avec succès !"); 
                    //videFrame();
                    int i=1;
                    centres=metier.findAllCentresD1Arrondissement(nomArrondissementSelected);
                    for(Centre c:centres){
                        model.insertRow(model.getRowCount(),new Object[]{c.getIdcent(),i++,c.getArrondissement().getDepartement().getRegion().getNomreg(), c.getArrondissement().getDepartement().getNomdepart(),c.getArrondissement().getNomarr(),c.getNomcent(),c.getEtablissement().getNometab(),c.getVersion()});
                    }
                }catch(ObcbudgetException ex){
                    JOptionPane.showMessageDialog(this, "Impossible de créer le centre de nom ["+nomCentreSaisi+"] \n raison:"+ex.getMessage()); 
                }
            }
        }
        else JOptionPane.showMessageDialog(this, " Le champ nom est obligatoire !"); 
    }//GEN-LAST:event_jButtonCréerActionPerformed

