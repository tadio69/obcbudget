    private void jButtonListerJurysActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListerJurysActionPerformed
        infoSaisies();
        Map<Centre, ArrayList<Jurydel>> centreJurydelMap = new HashMap<>();//contient les centres chacun avec le tableau de ses jurydel
        if (groupedexamensEnstring.equals("") || anneeString.equals("---")) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens et une session !");
        } else {
            anneeSelected = Integer.parseInt(anneeString);
            //remplissage de centreJurydelMap
            jurydels = metier.findAllJurydelsD1GroupedexamensD1Session(anneeSelected, niveauSelected, typeSelected);
            centres = metier.findAllCentres();
            if (!jurydels.isEmpty()) {
                for (Centre centre : centres) {
                    ArrayList<Jurydel> jurydelsList = new ArrayList<>();
                    for (Jurydel jurydel : jurydels) {
                        if (jurydel.getCentre().getNomcent().equals(centre.getNomcent())) {
                            jurydelsList.add(jurydel);
                        }
                    }
                    centreJurydelMap.put(centre, jurydelsList);
                }
            }

            if (!jurydels.isEmpty()) {
                try {
                    List<Map<String, Object>> datasource = new ArrayList<Map<String, Object>>();
                    for (Region region : regions) {
                        int i = 1;
                        for (Centre centre : centreJurydelMap.keySet()) {
                            if (centre.getEtablissement().getArrondissement().getDepartement().getRegion().getNomreg().equals(region.getNomreg())) {
                                for (Jurydel jurydel : centreJurydelMap.get(centre)) {
                                    Map<String, Object> row = new HashMap<>();
                                    row.put("session", anneeSelected);
                                    row.put("examen", groupedexamensEnstring);
                                    row.put("region", region.getNomreg());
                                    row.put("num", i++);
                                    row.put("centre", jurydel.getCentre().getNomcent());
                                    row.put("sousfamille", jurydel.getSousfamille().getTitre());
                                    row.put("nbjury", jurydel.getNbjurydel());
                                    datasource.add(row);
                                }
                            }
                        }
                    }
                    JRDataSource jRDataSource = new JRBeanCollectionDataSource(datasource);
                    JasperReport jasperReport = JasperCompileManager.compileReport("src\\main\\java\\reports\\etatListeJurydelReport.jrxml");
                    JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, jRDataSource);
                    JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);//false empêche jasperviewer de fermer l'appli lorsqu'on le ferme
                    jasperViewer.setVisible(true);
                } catch (JRException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Aucun nombre de jurys trouvé dans la base de données !");
            }
        }
    }//GEN-LAST:event_jButtonListerJurysActionPerformed

