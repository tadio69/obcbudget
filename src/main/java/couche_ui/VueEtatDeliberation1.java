    private void jButtonCreerJuryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreerJuryActionPerformed
        infoSaisies();
        if (groupedexamensEnstring.equals("") || anneeString.equals("---") || nomCentreSelected.equals("---") || titreSousfamilleSelected.equals("---") || nbjurysString.equals("")) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens, une session, un centre, une sous-famille, donner le nombre de jurys !");
        } else {
            validerNbjurys(nbjurysString);
            if (!continuerNbjurys || nbjurysSaisi == 0) {
                JOptionPane.showMessageDialog(this, "Recommencez svp. Le nombre de jurys fourni est incorrect !");
            } else {
                Groupe groupe = metier.findGroupeD1NiveauEtD1Type(niveauSelected, typeSelected);
                anneeSelected = Integer.parseInt(anneeString);
                for (Session s : sessions) {
                    if (s.getAnnee() == anneeSelected) {
                        sessionSelected = s;
                        break;
                    }
                }
                for (Centre centre : centres) {
                    if (centre.getNomcent().equals(nomCentreSelected)) {
                        centreSelected = centre;
                        break;
                    }
                }

                for (Sousfamille sousfam : sousfamilles) {
                    if (sousfam.getTitre().equals(titreSousfamilleSelected)) {
                        sousfamilleSelected = sousfam;
                        break;
                    }
                }
                try {
                    metier.creerJurydel(new Jurydel(nbjurysSaisi, centreSelected, groupe, sessionSelected, sousfamilleSelected));
                    JOptionPane.showMessageDialog(this, "Nombre de jurys enregistré avec succès !");
                } catch (ObcbudgetException ex) {
                    JOptionPane.showMessageDialog(this, "Impossible de d'enregistrer le nombre de jurys dans le centre [" + nomCentreSelected + "]" + "\n raison:" + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButtonCreerJuryActionPerformed

