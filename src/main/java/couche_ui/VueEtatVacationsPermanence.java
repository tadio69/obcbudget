    private void jButtonSupprimerPersonnelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSupprimerPersonnelActionPerformed
        infoSaisies();
        if (groupedexamensEnstring.equals("") || anneeString.equals("---") || nomsEtPrenomsSelected.equals("---")) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens, une session et un personnel !");
        } else {
            int result = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer ce membre de permanence", "confirmation de suppression", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (result == JOptionPane.YES_OPTION) {
                anneeSelected = Integer.parseInt(anneeString);
                tousEltpermanence = metier.findAllEltpermanenceD1Session(anneeSelected);

                try {
                    //le souscentre ici c'est OBC

                    for (Souscentre souscentre : souscentres) {
                        if (souscentre.getNomsouscent().equals("OFFICE DU BACCALAURÉAT DU CAMEROUN")) {
                            souscentreSelected = souscentre;
                            break;
                        }
                    }
                    Intervenant intervenant = metier.findIntervenantByNomEtPrenomD1GroupeDexamensD1SessionD1SouscentreD1ActiviteEt1Qualite(personnelSelected.getNompers(), personnelSelected.getPrenom(), anneeSelected, souscentreSelected.getNomsouscent(), niveauSelected, typeSelected, phase, "MEMBRE DE PERMANENCE");
                    Eltpermanence sonEltpermanence = new Eltpermanence();
                    for (Eltpermanence eltpermanence : tousEltpermanence) {
                        if (eltpermanence.getIntervenant().getPersonnel().getNompers().equals(intervenant.getPersonnel().getNompers()) && eltpermanence.getIntervenant().getPersonnel().getPrenom().equals(intervenant.getPersonnel().getPrenom())) {
                            sonEltpermanence = eltpermanence;
                        }
                    }
                    metier.supprimerEltpermanence(sonEltpermanence);
                    metier.supprimerIntervenant(intervenant);
                    JOptionPane.showMessageDialog(this, "Membre de permanence supprimé avec succès !");
                } catch (ObcbudgetException ex) {
                    JOptionPane.showMessageDialog(this, "Impossible de supprimer ce membre de permanence\n raison:" + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButtonSupprimerPersonnelActionPerformed

