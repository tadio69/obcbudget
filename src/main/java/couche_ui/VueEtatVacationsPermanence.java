    private void jButtonCreerPersonnelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreerPersonnelActionPerformed
        infoSaisies();
        if (groupedexamensEnstring.equals("") || anneeString.equals("---") || nomsEtPrenomsSelected.equals("---") || titreEpreuveSelected.equals("---") || nbjString.equals("") || listeExamensSelected.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens, une session, un personnel une épreuve, la liste d'examens concernés et donner le nombre de jours !");
        } else {
            Groupe groupe = metier.findGroupeD1NiveauEtD1Type(niveauSelected, typeSelected);
            anneeSelected = Integer.parseInt(anneeString);
            Intervenant intervenantCreated = null;
            for (Session s : sessions) {
                if (s.getAnnee() == anneeSelected) {
                    sessionSelected = s;
                    break;
                }
            }
            //le souscentre ici c'est OBC
            for (Souscentre souscentre : souscentres) {
                if (souscentre.getNomsouscent().equals("OFFICE DU BACCALAURÉAT DU CAMEROUN")) {
                    souscentreSelected = souscentre;
                    break;
                }
            }

            for (Epreuve epreuve : epreuves) {
                if (epreuve.getTitre().equals(titreEpreuveSelected)) {
                    epreuveSelected = epreuve;
                    break;
                }
            }

            validerNbjours(nbjString);
            boolean testSicreerIntervenant = false;
            boolean testSicreerEltpermanence = false;
            try {
                //personnelSelected est déterminé au moment du clic sur la jcombox
                Activite activite = metier.findActiviteByNom(phase);
                Qualite qualite = metier.findQualiteByTitre("MEMBRE DE PERMANENCE");
                boolean resident = false;
                intervenantCreated = new Intervenant(groupe, sessionSelected, personnelSelected, souscentreSelected, activite, qualite, resident);
                metier.creerIntervenant(intervenantCreated);
                testSicreerIntervenant = true;
            } catch (ObcbudgetException ex) {
                testSicreerIntervenant = false;
                JOptionPane.showMessageDialog(this, "Impossible de créer ce membre de permanence\n raison:" + ex.getMessage());
            }
            if (testSicreerIntervenant) {
                String examconcernes = String.join(", ", listeExamensSelected);//transformer la List<String> en String séparés de ,
                //examconcernes = examconcernes.replaceAll("\\[|\\]", "");//remplace [ et ] imposés à l'affichage par espece 
                metier.creerEltpermanence(new Eltpermanence(intervenantCreated, epreuveSelected, sessionSelected, examconcernes, nbjSaisi));
                testSicreerEltpermanence = true;
            }
            if (testSicreerEltpermanence) {
                JOptionPane.showMessageDialog(this, "Membre de permanenece et ses paramètres créés avec succès !");
            }
        }
    }//GEN-LAST:event_jButtonCreerPersonnelActionPerformed

    private void jButtonListerPersonnelsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListerPersonnelsActionPerformed
        infoSaisies();
        if (groupedexamensEnstring.equals("") || anneeString.equals("---")) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens et une session !");
        } else {
            anneeSelected = Integer.parseInt(anneeString);
            intervenants = metier.findAllIntervenantsD1GroupeDexamensD1SessionD1ActiviteEt1Qualite(anneeSelected, niveauSelected, typeSelected, phase, "MEMBRE DE PERMANENCE");
            tousEltpermanence = metier.findAllEltpermanenceD1Session(anneeSelected);
            if (!intervenants.isEmpty()) {
                try {
                    List<Map<String, Object>> datasource = new ArrayList<Map<String, Object>>();
                    int i = 1;
                    for (Eltpermanence eltpermanence : tousEltpermanence) {
                        Map<String, Object> row = new HashMap<>();
                        row.put("session", anneeSelected);
                        row.put("examen", groupedexamensEnstring);
                        row.put("num", i++);
                        row.put("pers", eltpermanence.getIntervenant().getPersonnel().getNompers() + " " + eltpermanence.getIntervenant().getPersonnel().getPrenom());
                        row.put("epreuve", eltpermanence.getEpreuve().getTitre());
                        row.put("examconcern", eltpermanence.getExamconcern());
                        row.put("nbj", eltpermanence.getNbj());
                        datasource.add(row);
                    }
                    JRDataSource jRDataSource = new JRBeanCollectionDataSource(datasource);
                    JasperReport jasperReport = JasperCompileManager.compileReport("src\\main\\java\\reports\\etatListeParametresEltpermanenceReport.jrxml");
                    JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, jRDataSource);
                    JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);//false empêche jasperviewer de fermer l'appli lorsqu'on le ferme
                    jasperViewer.setVisible(true);
                } catch (JRException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            }
        }
        intervenants.clear();
        tousEltpermanence.clear();
    }//GEN-LAST:event_jButtonListerPersonnelsActionPerformed

