    private void jButtonCreerEtatsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreerEtatsActionPerformed
        infoIntervenantSaisies();
        Map<Souscentre, ArrayList<Intervenantsecret>> souscentreIntervenantsecretMap = new HashMap<>();//contient les sous-centres chacun avec le tableau de ses Intervenantsecret
        Map<Souscentre, ArrayList<Intervenant>> souscentreIntervenantsMap = new HashMap<>();//contient les sous-centres chacun avec la liste de ses intervenants
        ArrayList<Souscentre> souscentresIntervenantsList = new ArrayList<>();//liste de tous les sous-centres ayant des intervenants dans le groupedexamensEnstring dans la session considérée
        ArrayList<Souscentre> souscentresIntervenantsecretsList = new ArrayList<>();//liste de tous les sous-centres ayant des intervenantsecrets dans le groupedexamensEnstring dans la session considérée
        ArrayList<Souscentre> souscentresList = new ArrayList<>();//liste de tous les sous-centres ayant des intervenants et intervenantsecrets dans le groupedexamensEnstring dans la session considérée
        if (!groupedexamensEnstring.equals("") && !anneeString.equals("---") && !titreQualiteSelected.equals("---")) {
            Groupe groupe = metier.findGroupeD1NiveauEtD1Type(niveauSelected, typeSelected);
            anneeSelected = Integer.parseInt(anneeString);
            String rubrique = "";
            if (titreQualiteSelected.equals("ANONYMISATEUR DE NOTES") || titreQualiteSelected.equals("ENREGISTREUR DE NOTES")) {
                rubrique = "PRISE EN CHARGE DES ANONYMISATEURS ET ENREGISTREURS DES NOTES";
            }
            Rubriquetat rubetat = metier.findRubriquetatByDesignation(rubrique);
            caisses = metier.findAllCaissesD1GroupedexamensDans1RubriquetatEt1Session(rubetat.getDesignation(), anneeSelected, niveauSelected, typeSelected);
            if (!caisses.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Erreur, ces états existent déjà pour ce groupe d'examens !");
            } else {
                for (Session s : sessions) {
                    if (s.getAnnee() == anneeSelected) {
                        sessionSelected = s;
                        break;
                    }
                }

                intervenants = metier.findAllIntervenantsD1GroupeDexamensD1SessionD1ActiviteEt1Qualite(anneeSelected, niveauSelected, typeSelected, phase, titreQualiteSelected);
                desIntervenantsecret = metier.findAllIntervenantsecretD1GroupedexamensD1SessionD1ActiviteEt1Qualite(anneeSelected, niveauSelected, typeSelected, phase, titreQualiteSelected);

                souscentres = metier.findAllSouscentres();
                for (Souscentre scentre : souscentres) {
                    for (Intervenant interv : intervenants) {
                        if (!souscentresIntervenantsList.contains(scentre) && interv.getSouscentre().getNomsouscent().equals(scentre.getNomsouscent())) {
                            souscentresIntervenantsList.add(scentre);
                        }
                    }
                    for (Intervenantsecret intersec : desIntervenantsecret) {
                        if (!souscentresIntervenantsecretsList.contains(scentre) && intersec.getSecretariat().getSouscentre().getNomsouscent().equals(scentre.getNomsouscent())) {
                            souscentresIntervenantsecretsList.add(scentre);
                        }
                    }
                }

                for (Souscentre scentre : souscentresIntervenantsecretsList) {
                    ArrayList<Intervenantsecret> intervenantsecretsList = new ArrayList<>();
                    for (Intervenantsecret intersec : desIntervenantsecret) {
                        if (intersec.getSecretariat().getSouscentre().getNomsouscent().equals(scentre.getNomsouscent())) {
                            intervenantsecretsList.add(intersec);
                        }
                    }
                    souscentreIntervenantsecretMap.put(scentre, intervenantsecretsList);
                }

                for (Souscentre scentre : souscentresIntervenantsList) {
                    ArrayList<Intervenant> intervenantsList = new ArrayList<>();
                    for (Intervenant inter : intervenants) {
                        if (inter.getSouscentre().getNomsouscent().equals(scentre.getNomsouscent())) {
                            intervenantsList.add(inter);
                            break;
                        }
                    }
                    souscentreIntervenantsMap.put(scentre, intervenantsList);
                }

                //on met tous les sous-centres dans souscentresList
                for (Souscentre scentre : souscentresIntervenantsList) {
                    if (!souscentresList.contains(scentre)) {
                        souscentresList.add(scentre);
                    }
                }
                for (Souscentre scentre : souscentresIntervenantsecretsList) {
                    if (!souscentresList.contains(scentre)) {
                        souscentresList.add(scentre);
                    }
                }

                boolean testsicreer = false;

                if (typeSelected.equals("STT") || typeSelected.equals("STT PROF")) {//cas STT
                    try {
                        for (Souscentre scentre : souscentreIntervenantsMap.keySet()) {
                            int net = 0;
                            int totalArrondi = 0;
                            //ArrayList<Jurydel> jurydelList = centreJurydelMap.get(centre);
                            int nbj = 0;
                            for (int p = 0; p <= nbj; p++) {
                                if (p == nbj) {//traitement des jurys temoins et des épreuves générales (EG)
                                    //12 membres par jury temoin
                                    //10 membres pour EG
                                    //nbjurys = centreJurytemoinMap.get(centre);
                                    int nbmbresJT = 12 * nbjurys;
                                    nbvac = nbmbresJT * 4;//4 vacations par mbre
                                    coutvac = nbvac * txvac;
                                    nbteneurs = nbjurys;//1 teneur PV physique par JT. pas de teneur num ici
                                    primevice = 0;
                                    primeteneurPhys = nbteneurs * 10000;//pour tous les teneurs. chacun a 10000
                                    primeteneurNum = 0;
                                    //coutEG= 10*8*1500 pour 10 membres, 8vac/mbre
                                    total = coutvac + primeteneurPhys + 10 * 8 * 1500;
                                    totalArrondi = (int) Math.ceil((double) total / (double) 1000) * 1000;
                                    net += totalArrondi;
                                } else {
                                    //determinerCoutD1jurydel(jurydelList.get(p));
                                    totalArrondi = (int) Math.ceil((double) total / (double) 1000) * 1000;
                                    net += totalArrondi;
                                }
                            }
                            //Souscentre souscentre = metier.findSouscentreByEtablissement(centre.getEtablissement().getAbreviation())
 
                            metier.creerCaisse(new Caisse(net, 0, 0, 0, groupe, rubetat, sessionSelected, scentre, false));
                            testsicreer = true;
                        }
                    } catch (ObcbudgetException e) {
                        JOptionPane.showMessageDialog(this, e.getMessage());
                    }
                    if (testsicreer) {
                        JOptionPane.showMessageDialog(this, "Etats de délibération créés avec succès pour ce groupe d'examens !");
                    }
                }
                examensSelected.clear();//on vide

            }
        } else {
            JOptionPane.showMessageDialog(this, "Les champs examen(s), session et qualité sont obligatoires !");
        }
    }//GEN-LAST:event_jButtonCreerEtatsActionPerformed

