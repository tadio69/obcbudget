            Map<Souscentre, ArrayList<Examen>> souscentreIndMap = new HashMap<>();//contient les souscentres chacun avec le tableau de ses examens industriels
            Map<Souscentre, String> souscentreNomFinalMap = new HashMap<>();;//contient l'ensemble des sous-centres issus des statistiques; il distingue les urbains des interurbains
            List<Souscentre> souscentreList = new ArrayList<>();//contient l'ensemble des sous-centres issus des rattachements de la session 
