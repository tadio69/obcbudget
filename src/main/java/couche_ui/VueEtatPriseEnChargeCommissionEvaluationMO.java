    private void jButtonCreerPersonnelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreerPersonnelActionPerformed
        infoSaisies();
        if (groupedexamensEnstring.equals("") || anneeString.equals("---") || nomOptionSelected.equals("") || titreSousfamilleSelected.equals("") || numtel.equals("") || nbcasString.equals("")) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens, une session, un personnel et les 4 paramètres !");
        } else {
            Groupe groupe = metier.findGroupeD1NiveauEtD1Type(niveauSelected, typeSelected);
            anneeSelected = Integer.parseInt(anneeString);
            Intervenant intervenantCreated = null;
            for (Session s : sessions) {
                if (s.getAnnee() == anneeSelected) {
                    sessionSelected = s;
                    break;
                }
            }
            //le souscentre ici c'est OBC

            for (Souscentre souscentre : souscentres) {
                if (souscentre.getNomsouscent().equals("OFFICE DU BACCALAURÉAT DU CAMEROUN")) {
                    souscentreSelected = souscentre;
                    break;
                }
            }
            for (Option option : options) {
                if (option.getSigle().equals(nomOptionSelected)) {
                    optionSelected = option;
                }
            }
            for (Sousfamille sousfamille : sousfamilles) {
                if (sousfamille.getTitre().equals(titreSousfamilleSelected)) {
                    sousfamilleSelected = sousfamille;
                }
            }
            validerNbcas(nbcasString);
            boolean testSicreerIntervenant = false;
            boolean testSicreerEltcomeval = false;
            try {
                //personnelSelected est déterminé au moment du clic sur la jcombox
                Activite activite = metier.findActiviteByNom(phase);
                Qualite qualite = metier.findQualiteByTitre("MEMBRE DE LA COMMISSION D'EVALUATION DE LA MATIERE D'OEUVRE");
                boolean resident = false;
                intervenantCreated = new Intervenant(groupe, sessionSelected, personnelSelected, souscentreSelected, activite, qualite, resident);
                metier.creerIntervenant(intervenantCreated);
                testSicreerIntervenant = true;
            } catch (ObcbudgetException ex) {
                testSicreerIntervenant = false;
                JOptionPane.showMessageDialog(this, "Impossible de créer ce membre de la commission de la matière d'oeuvre\n raison:" + ex.getMessage());
            }
            if (testSicreerIntervenant) {
                metier.creerEltcomeval(new Eltcomeval(groupe, sessionSelected, intervenantCreated, optionSelected, numtel, nbcasSaisi, sousfamilleSelected));
                testSicreerEltcomeval = true;
            }
            if (testSicreerEltcomeval) {
                JOptionPane.showMessageDialog(this, "Membre de la commission de la matière d'oeuvre et ses paramètres créés avec succès !");
            }
        }
    }//GEN-LAST:event_jButtonCreerPersonnelActionPerformed

    private void jButtonListerPersonnelsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListerPersonnelsActionPerformed
        infoSaisies();
        if (groupedexamensEnstring.equals("") || anneeString.equals("---")) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens et une session !");
        } else {
            anneeSelected = Integer.parseInt(anneeString);
            membres = metier.findAllIntervenantsD1GroupeDexamensD1SessionD1ActiviteEt1Qualite(anneeSelected, niveauSelected, typeSelected, phase, "MEMBRE DE LA COMMISSION D'EVALUATION DE LA MATIERE D'OEUVRE");
            tousEltcomeval = metier.findAllEltcomevalD1GroupedexamensD1Session(anneeSelected, niveauSelected, typeSelected);
            if (!membres.isEmpty()) {
                try {
                    List<Map<String, Object>> datasource = new ArrayList<Map<String, Object>>();
                    int i = 1;
                    for (Eltcomeval eltcomeval : tousEltcomeval) {
                        Map<String, Object> row = new HashMap<>();
                        row.put("session", anneeSelected);
                        row.put("examen", groupedexamensEnstring);
                        row.put("num", i++);
                        row.put("pers", eltcomeval.getIntervenant().getPersonnel().getNompers() + " " + eltcomeval.getIntervenant().getPersonnel().getPrenom());
                        row.put("option", eltcomeval.getOption().getSigle());
                        row.put("numtel", eltcomeval.getNumtel());
                        row.put("nbcas", eltcomeval.getNbcas());
                        row.put("sousfamille", eltcomeval.getSousfamille().getTitre());
                        datasource.add(row);
                    }
                    JRDataSource jRDataSource = new JRBeanCollectionDataSource(datasource);
                    JasperReport jasperReport = JasperCompileManager.compileReport("src\\main\\java\\reports\\etatListeParametresComevalReport.jrxml");
                    JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, jRDataSource);
                    JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);//false empêche jasperviewer de fermer l'appli lorsqu'on le ferme
                    jasperViewer.setVisible(true);
                } catch (JRException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            }
        }
        tousEltcomeval.clear();
        membres.clear();
    }//GEN-LAST:event_jButtonListerPersonnelsActionPerformed

