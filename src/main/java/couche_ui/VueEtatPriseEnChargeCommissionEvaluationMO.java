    private void remplirSousfamilleCombobox() {
        titreFamilleSelected = (String) jComboBoxFamille.getSelectedItem();
        sousfamilles = metier.findAllSousfamillesD1Famille(titreFamilleSelected);
        //on remplit la combobox des sousfamilles
        sousfamillesCombo = new String[sousfamilles.size() + 1];
        sousfamillesCombo[0] = "---";
        int i = 1;
        for (Sousfamille sousfamille : sousfamilles) {
            sousfamillesCombo[i++] = sousfamille.getTitre();
        }
        jComboBoxSousfamille.setModel(new DefaultComboBoxModel(sousfamillesCombo));
    }
