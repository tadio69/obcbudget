    private void jButtonListerNombresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListerNombresActionPerformed
        infoSaisies();
        if (groupedexamensEnstring.equals("") || anneeString.equals("---")) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens et une session !");
        } else {
            anneeSelected = Integer.parseInt(anneeString);
            eltcorrecttechs = metier.findAllEltcorrecttechsD1GroupedexamensD1Session(anneeSelected, niveauSelected, typeSelected);

            if (!eltcorrecttechs.isEmpty()) {
                try {
                    List<Map<String, Object>> datasource = new ArrayList<Map<String, Object>>();
                    for (Region region : regions) {
                        int i = 1;
                        for (Eltcorrecttech eltcorrecttech : eltcorrecttechs) {
                            if (eltcorrecttech.getSouscentre().getEtablissement().getArrondissement().getDepartement().getRegion().getNomreg().equals(region.getNomreg())) {
                                Map<String, Object> row = new HashMap<>();
                                row.put("session", anneeSelected);
                                row.put("examen", groupedexamensEnstring);
                                row.put("region", region.getNomreg());
                                row.put("num", i++);
                                row.put("souscentre", eltcorrecttech.getSouscentre().getNomsouscent());
                                row.put("sousfamille", eltcorrecttech.getPonderation().getSousfamille().getTitre());
                                row.put("effectif", eltcorrecttech.getEffectif());
                                row.put("nature", eltcorrecttech.getPonderation().getNature());
                                row.put("nbmat", eltcorrecttech.getPonderation().getNbmat());
                                row.put("coef", eltcorrecttech.getPonderation().getCoef());
                                row.put("nbchefsal", eltcorrecttech.getNbchefsal());
                                datasource.add(row);
                            }
                        }
                    }
                    JRDataSource jRDataSource = new JRBeanCollectionDataSource(datasource);
                    JasperReport jasperReport = JasperCompileManager.compileReport("src\\main\\java\\reports\\etatListeEltcorrecttechReport.jrxml");
                    JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, jRDataSource);
                    JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);//false empêche jasperviewer de fermer l'appli lorsqu'on le ferme
                    jasperViewer.setVisible(true);
                } catch (JRException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Aucun nombre de candidats/chefs de salle trouvé dans la base de données !");
            }
        }
    }//GEN-LAST:event_jButtonListerNombresActionPerformed

