    private void determinerCoutD1Eltcorrecttech(Eltcorrecttech eltcorrecttech) {
        //détermination de txcopie
        niveauSelected = (String) jComboBoxNiveau.getSelectedItem();
        if (niveauSelected.equals("PROB")) {
            txcopie = 150;
        }
        if (niveauSelected.equals("BACC")) {
            txcopie = 200;
        }

        nbmat = eltcorrecttech.getPonderation().getNbmat();
        coef = eltcorrecttech.getPonderation().getCoef();
        totalpond = nbmat + coef;
        effectif = eltcorrecttech.getEffectif();
        nbcopies = effectif * totalpond;
        coutcopies = (int) nbcopies * txcopie;
        nbchefsal = eltcorrecttech.getNbchefsal();
        indemnite = nbchefsal * txIndemnite;
        supplement =(int) nbcopies * txSupplement;
        total = coutcopies + indemnite + supplement;
    }

