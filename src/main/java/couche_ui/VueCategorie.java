    private void jButtonCreerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreerActionPerformed
        model.setRowCount(0);//vide la jTable
        infoSaisies();           
        if(!titreSaisi.equals("")){
            try{
                metier.creerCategorie(new Categorie(titreSaisi.toUpperCase()));
                JOptionPane.showMessageDialog(this, " Categorie créée avec succès !"); 
                //videFrame();
                categories=metier.findAllCategories();
                int i=1;
                for(Categorie c:categories){
                    model.insertRow(model.getRowCount(),new Object[]{c.getIdcat(), i++,c.getTitre(),c.getVersion()});
                }
            }catch(ObcbudgetException ex){
                JOptionPane.showMessageDialog(this, "Impossible de créer la catégorie de titre ["+titreSaisi+"] \n raison:"+ex.getMessage());
            }
        }
        else JOptionPane.showMessageDialog(this, " Le champ titre est obligatoire !");  
    }//GEN-LAST:event_jButtonCreerActionPerformed

    private void jButtonRechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRechercherActionPerformed
        model.setRowCount(0);//vide la jTable
        infoSaisies();       
        if(!titreSaisi.equals("") && !titreSaisi.equalsIgnoreCase("toutes")){
            try{
                categoriesRecherchees=metier.findAllCategoriesLikeTitre(titreSaisi.toUpperCase());
                int i=1;
                for(Categorie c:categoriesRecherchees){
                    model.insertRow(model.getRowCount(),new Object[]{c.getIdcat(), i++,c.getTitre(),c.getVersion()});
                } 
            }catch(ObcbudgetException ex){
                    JOptionPane.showMessageDialog(this, "Impossible de trouver des catégories de titre proche de ["+titreSaisi+"] \n raison:"+ex.getMessage()); 
            } 
        }
        else{
            if(titreSaisi.equals("")) JOptionPane.showMessageDialog(this, " Le champ titre est obligatoire !");
            else{
                categories=metier.findAllCategories();
                int i=1;
                for(Categorie c:categories){
                    model.insertRow(model.getRowCount(),new Object[]{c.getIdcat(), i++,c.getTitre(),c.getVersion()});
                }
            }
        }
    }//GEN-LAST:event_jButtonRechercherActionPerformed

