    private void jButtonCreerNbadmissibActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreerNbadmissibActionPerformed
        infoSaisies();
        if (jRadioButtonRELIQUAT.isSelected()) {
            if (groupedexamensEnstring.equals("") || anneeString.equals("---") || nomSouscentreSelected.equals("---") || sigleOptionNbadmissibSelected.equals("---") || nbadmissibString.equals("")) {
                JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens, une session, un sous-centre, sélectionner une spécialité et donner le nombre de candidats admissibles !");
            } else {
                validerNbadmissib(nbadmissibString);
                if (!continuerNbadmissib || nbadmissibSaisi == 0) {
                    JOptionPane.showMessageDialog(this, "Recommencez svp. Le nombre de candidats admissibles est incorrect !");
                } else {
                    Groupe groupe = metier.findGroupeD1NiveauEtD1Type(niveauSelected, typeSelected);
                    anneeSelected = Integer.parseInt(anneeString);
                    for (Session s : sessions) {
                        if (s.getAnnee() == anneeSelected) {
                            sessionSelected = s;
                            break;
                        }
                    }
                    for (Souscentre souscentre : souscentres) {
                        if (souscentre.getNomsouscent().equals(nomSouscentreSelected)) {
                            souscentreSelected = souscentre;
                            break;
                        }
                    }

                    for (Option option : options) {
                        if (option.getSigle().equals(sigleOptionNbadmissibSelected)) {
                            optionSelected = option;
                            break;
                        }
                    }
                    try {
                        metier.creerNbadmissibles(new Nbadmissibles(souscentreSelected, sessionSelected, groupe, optionSelected, nbadmissibSaisi));
                        JOptionPane.showMessageDialog(this, "Nombre de candidats admissibles enregistré avec succès !");
                    } catch (ObcbudgetException ex) {
                        JOptionPane.showMessageDialog(this, "Impossible de d'enregistrer le nombre de candidats admissibles dans le sous-centre [" + nomSouscentreSelected + "]" + "\n raison:" + ex.getMessage());
                    }
                }
            }
        }
    }//GEN-LAST:event_jButtonCreerNbadmissibActionPerformed

    private void jButtonListerNbadmissibActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListerNbadmissibActionPerformed
        infoSaisies();
        if (jRadioButtonRELIQUAT.isSelected()) {
            if (groupedexamensEnstring.equals("") || anneeString.equals("---")) {
                JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens et une session !");
            } else {
                anneeSelected = Integer.parseInt(anneeString);
                nbadmissibles = metier.findAllNbadmissiblesD1GroupedexamensD1Session(anneeSelected, niveauSelected, typeSelected);

                if (!nbadmissibles.isEmpty()) {
                    try {
                        List<Map<String, Object>> datasource = new ArrayList<Map<String, Object>>();
                        for (Region region : regions) {
                            int i = 1;
                            for (Nbadmissibles nbadmiss : nbadmissibles) {
                                if (nbadmiss.getSouscentre().getEtablissement().getArrondissement().getDepartement().getRegion().getNomreg().equals(region.getNomreg())) {
                                    Map<String, Object> row = new HashMap<>();
                                    row.put("session", anneeSelected);
                                    row.put("examen", groupedexamensEnstring);
                                    row.put("region", region.getNomreg());
                                    row.put("num", i++);
                                    row.put("souscentre", nbadmiss.getSouscentre().getNomsouscent());
                                    row.put("option", nbadmiss.getOption().getSigle());
                                    row.put("nbadmissibles", nbadmiss.getNbadmiss());
                                    datasource.add(row);
                                }
                            }
                        }
                        JRDataSource jRDataSource = new JRBeanCollectionDataSource(datasource);
                        JasperReport jasperReport = JasperCompileManager.compileReport("src\\main\\java\\reports\\etatListeNbadmissiblesReport.jrxml");
                        JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, jRDataSource);
                        JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);//false empêche jasperviewer de fermer l'appli lorsqu'on le ferme
                        jasperViewer.setVisible(true);
                    } catch (JRException e) {
                        JOptionPane.showMessageDialog(this, e.getMessage());
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Aucun nombre d'admissibles, de vacations, d'examinateurs et de chefs d'atelier trouvé dans la base de données !");
                }
            }
        }
    }//GEN-LAST:event_jButtonListerNbadmissibActionPerformed

