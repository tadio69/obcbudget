    private void determinerCoutAvance(Map<Souscentre, ArrayList<Statistique>> souscentreStatistiqueMap, Souscentre souscentre) {
        statInscrit = 0;
        totalAvance = 0;
        for (Statistique stat : souscentreStatistiqueMap.get(souscentre)) {
            if (stat.getOption().getSigle().equals("F1") || stat.getOption().getSigle().equals("F2") || stat.getOption().getSigle().equals("F3") || stat.getOption().getSigle().equals("F5") || stat.getOption().getSigle().equals("F4-BA") || stat.getOption().getSigle().equals("MF-CM") || stat.getOption().getSigle().equals("EF") || stat.getOption().getSigle().equals("TGF") || stat.getOption().getSigle().equals("MEB")) {
                statInscrit += stat.getNombre();
            }
        }
        if (niveauSelected.equals("PROB")) {
            txMO = 7000;
            if (statInscrit > 0 && statInscrit <= 50) {
                pourcentage = 0.5;
            }
            if (statInscrit > 50 && statInscrit <= 100) {
                pourcentage = 0.4;
            }
            if (statInscrit > 100 && statInscrit <= 500) {
                pourcentage = 0.2;
            }
            if (statInscrit > 500 && statInscrit <= 1000) {
                pourcentage = 0.15;
            }
            if (statInscrit > 1000) {
                pourcentage = 0.1;
            }
        }
        if (niveauSelected.equals("BACC")) {
            txMO = 8000;
            if (statInscrit > 0 && statInscrit <= 50) {
                pourcentage = 0.5;
            }
            if (statInscrit > 50 && statInscrit <= 100) {
                pourcentage = 0.4;
            }
            if (statInscrit > 100 && statInscrit <= 500) {
                pourcentage = 0.3;
            }
            if (statInscrit > 500 && statInscrit <= 1000) {
                pourcentage = 0.2;
            }
        }

        totalAvance = (int) (statInscrit * txMO * pourcentage);
    }

    private void determinerCoutD1Mopratiquesti(Mopratiquesti mopratiquesti, Map<Souscentre, ArrayList<Nbadmissibles>> souscentreNbadmissiblesMap) {
        statOption = 0;
        totalMopratiquesti = 0;
        specialitePourAffichage = mopratiquesti.getOption().getSigle();
        for (Nbadmissibles nbadmiss : souscentreNbadmissiblesMap.get(mopratiquesti.getSouscentre())) {
            if (nbadmiss.getOption().getSigle().equals(specialitePourAffichage)) {
                statOption = nbadmiss.getNbadmiss();
                break;
            }
        }
        if (statOption > 0) {
            totalMopratiquesti = statOption * mopratiquesti.getCoutcand() + mopratiquesti.getForfait() + statOption * mopratiquesti.getCoutamort() + mopratiquesti.getNbpostes() * mopratiquesti.getCoutposte();
        }
    }
