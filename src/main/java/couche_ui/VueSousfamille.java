    private void jButtonModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModifierActionPerformed
        model.setRowCount(0);//vide la jTable
        infoSaisies(); 
        int result = JOptionPane.showConfirmDialog(this,"Voulez-vous vraiment modifier cette sous-famille?", "confirmation de modification",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(result == JOptionPane.YES_OPTION){
            if(!titreSaisi.equals("")&& !titreFamilleSelected.equals("---")){
                for(Famille f:familles){
                    if(f.getTitre().equals(titreFamilleSelected)){
                        familleSelected=f;
                        break;
                    }
                }
                sousfamilleCliked=metier.findSousfamilleById(idSousfamilleCliked);
                sousfamilleCliked.setTitre(titreSaisi.toUpperCase());
                sousfamilleCliked.setFamille(familleSelected);
                try{
                    metier.modifierSousfamille(sousfamilleCliked);
                    JOptionPane.showMessageDialog(this, " Sous-famille modifiée avec succès !");
                    //videFrame();
                    sousfamilles=metier.findAllSousfamilles();
                    int i=1;
                    for(Sousfamille s:sousfamilles){
                        model.insertRow(model.getRowCount(),new Object[]{s.getIdsousfam(),i++, s.getTitre(),s.getFamille().getTitre(),s.getVersion()});
                    } 
                }catch(ObcbudgetException ex){
                        JOptionPane.showMessageDialog(this, "Impossible de modifier la sous-famille de titre proche de ["+titreSaisi+"] \n raison:"+ex.getMessage()); 
                    }                                   
            }
            else JOptionPane.showMessageDialog(this, " Tous les champs sont obligatoires !"); 
        }
        /*
        else if (result == JOptionPane.NO_OPTION){
               label.setText("You selected: No");
            }
        */ 
    }//GEN-LAST:event_jButtonModifierActionPerformed

