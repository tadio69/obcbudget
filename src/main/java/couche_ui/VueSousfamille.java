    private void jButtonCreerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreerActionPerformed
        model.setRowCount(0);//vide la jTable
        infoSaisies();           
        if(!titreSaisi.equals("") && !titreFamilleSelected.equals("---")){
            for(Famille f:familles){
                if(f.getTitre().equals(titreFamilleSelected)){
                    familleSelected=f;
                    break;
                }
            }
            try{
                metier.creerSousfamille(new Sousfamille(titreSaisi.toUpperCase(),familleSelected));
                JOptionPane.showMessageDialog(this, " Sous-famille créée avec succès !"); 
                sousfamilles=metier.findAllSousfamilles();
                int i=1;
                for(Sousfamille s:sousfamilles){
                    model.insertRow(model.getRowCount(),new Object[]{s.getIdsousfam(),i++, s.getTitre(),s.getFamille().getTitre(),s.getVersion()});
                }
            }catch(ObcbudgetException ex){
                JOptionPane.showMessageDialog(this, "Impossible de créer la sous-famille de titre ["+titreSaisi+"] \n raison:"+ex.getMessage());
            }
        }
        else JOptionPane.showMessageDialog(this, " Tous les champs sont obligatoires !"); 
    }//GEN-LAST:event_jButtonCreerActionPerformed

    private void jButtonRechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRechercherActionPerformed
        model.setRowCount(0);//vide la jTable
        infoSaisies();       
        if(!titreSaisi.equals("") && !titreSaisi.equalsIgnoreCase("toutes")){
            try{
                sousfamillesRecherchees=metier.findAllSousfamillesLikeTitre(titreSaisi.toUpperCase());
                int i=1;
                for(Sousfamille s:sousfamillesRecherchees){
                    model.insertRow(model.getRowCount(),new Object[]{s.getIdsousfam(),i++, s.getTitre(),s.getFamille().getTitre(),s.getVersion()});
                } 
            }catch(ObcbudgetException ex){
                    JOptionPane.showMessageDialog(this, "Impossible de trouver des sous-familles de titre proche de ["+titreSaisi+"] \n raison:"+ex.getMessage()); 
            } 
        }
        else{
            if(titreSaisi.equals("")) JOptionPane.showMessageDialog(this, " Le champ titre est obligatoire !");
            else{
                sousfamilles=metier.findAllSousfamilles();
                int i=1;
                for(Sousfamille s:sousfamilles){
                    model.insertRow(model.getRowCount(),new Object[]{s.getIdsousfam(),i++, s.getTitre(),s.getFamille().getTitre(),s.getVersion()});
                } 
            }
        }  
    }//GEN-LAST:event_jButtonRechercherActionPerformed

