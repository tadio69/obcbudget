    private void jButtonCreerEffectifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreerEffectifActionPerformed
        infoSaisies();
        if (groupedexamensEnstring.equals("") || anneeString.equals("---") || nomSouscentreSelected.equals("---") || effectifString.equals("") || sigleEpreuvefacSelected.equals("---")) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens, une session, un sous-centre, une épreuve et donner l'effectif de candidats !");
        } else {
            validerEffectif(effectifString);
            if (!continuer || effectifSaisi == 0) {
                JOptionPane.showMessageDialog(this, "Recommencez svp. L'effectif de candidats fourni est incorrect !");
            } else {
                Groupe groupe = metier.findGroupeD1NiveauEtD1Type(niveauSelected, typeSelected);
                anneeSelected = Integer.parseInt(anneeString);
                for (Session s : sessions) {
                    if (s.getAnnee() == anneeSelected) {
                        sessionSelected = s;
                        break;
                    }
                }
                for (Souscentre souscentre : souscentres) {
                    if (souscentre.getNomsouscent().equals(nomSouscentreSelected)) {
                        souscentreSelected = souscentre;
                        break;
                    }
                }

                for (Epreuve epfac : epreuvesfacs) {
                    if (epfac.getSigle().equals(sigleEpreuvefacSelected)) {
                        epreuvefacSelected = epfac;
                        break;
                    }
                }
                try {
                    metier.creerEltetatmoepreuvefac(new Eltetatmoepreuvefac(souscentreSelected, groupe, sessionSelected, epreuvefacSelected, effectifSaisi));
                    JOptionPane.showMessageDialog(this, "Effectif de candidats enregistré avec succès !");
                } catch (ObcbudgetException ex) {
                    JOptionPane.showMessageDialog(this, "Impossible de d'enregistrer l'effectif de candidats dans le sous-centre [" + nomSouscentreSelected + "]" + "\n raison:" + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButtonCreerEffectifActionPerformed

