    private void determinerCoutD1eltetatmoepreuvefac(Eltetatmoepreuvefac eltetatmoepreuvefac) {
        int effectif = eltetatmoepreuvefac.getEffectif();
        Epreuve epfac = eltetatmoepreuvefac.getEpreuve();
        if (epfac.getSigle().equals("TP INFO")) {
            if (effectif > 0 && effectif < 20) {
                total = 10000;
            }
            if (effectif >= 20 && effectif <= 400) {
                total = 50000;
            }
            if (effectif > 400 && effectif <= 500) {
                total = 55000;
            }
            if (effectif > 500) {
                total = 60000;
            }
        }

        if (epfac.getSigle().equals("DESSIN")) {
            if (effectif <0) {
                total = 0;
            }
            if (effectif >= 20 && effectif <= 400) {
                total = 5000;
            }
            if (effectif > 400 && effectif <= 800) {
                total = 10000;
            }
            if (effectif > 800 && effectif <= 1200) {
                total = 15000;
            }
        }
        /*
        if (epfac.getSigle().equals("ESF")) {
            int forfait = 10000;
            int gaz = 6500;
            if (effectif > 0 && effectif <= 40) {
                total = gaz + forfait;
            }
            if (effectif > 40 && effectif <= 80) {
                total = 2 * gaz + forfait;
            }
            if (effectif > 80 && effectif <= 120) {
                total = 3 * gaz + forfait;
            }
            if (effectif > 120 && effectif <= 160) {
                total = 4 * gaz + forfait;
            }
            if (effectif > 160 && effectif <= 200) {
                total = 5 * gaz + forfait;
            }
        }
        */

        if (epfac.getSigle().equals("ESF") || epfac.getSigle().equals("TP CHIMIE") || epfac.getSigle().equals("TP PHYSIQUE") || epfac.getSigle().equals("TP SVT") || epfac.getSigle().equals("MUSIQUE")) {
            total = 100 * effectif;
        }
    }

    private void remplirExamensTextArea() {
        typeSelected = (String) jComboBoxType.getSelectedItem();
        String examensSelectedEnchaine = "";
        if (typeSelected.equals("---")) {
            JOptionPane.showMessageDialog(this, "Le champ Type est vide !");
        } else {
            groupedexamens = metier.findAllGroupedexamensD1NiveauEtD1Type(niveauSelected, typeSelected);
            int i = 0;
            for (Groupedexamen g : groupedexamens) {
                if (i++ == 0) {
                    examensSelectedEnchaine += g.getExamen().getSigle();
                } else {
                    examensSelectedEnchaine += ", " + g.getExamen().getSigle();
                }
            }
            jTextArea1.setText(examensSelectedEnchaine);
            jTextArea1.setLineWrap(true);//renvoyer automatiquement le texte à la ligne dans le textArea
        }
    }

