    private void jMenuItemQuitterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemQuitterActionPerformed
        boolean backupOK=false;
        int result = JOptionPane.showConfirmDialog(this, "Voulez-vous sauvegarder la base de donn√©es maintenant ?", "confirmation de sauvegarde de la base de donn√©es", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (result == JOptionPane.OK_OPTION) {
            try{
                realiserBackup();
                backupOK=true;
                 dispose();
            }catch(IOException e){
                e.printStackTrace();
            }catch(InterruptedException ie){
                ie.printStackTrace();
            }            
        }
        if(!backupOK){
            int result1 = JOptionPane.showConfirmDialog(this, "La sauvegarde de la base de donn√©es a √©chou√©. Voulez-vous tout de m√™me fermer l'application ?", "confirmation de fermeture de l'application ", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (result1 == JOptionPane.OK_OPTION) {
                dispose();
            }
        }
    }//GEN-LAST:event_jMenuItemQuitterActionPerformed

