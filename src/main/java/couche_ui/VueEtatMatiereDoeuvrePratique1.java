    private void jButtonCreerCoutESFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreerCoutESFActionPerformed
        infoSaisies();
        if (groupedexamensEnstring.equals("") || anneeString.equals("---") || nomSouscentreSelected.equals("---") || coutesfString.equals("")) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens, une session, un sous-centre, donner le coût par candidat ESF !");
        } else {
            validerCoutesf(coutesfString);
            if (!continuerCoutesf) {
                JOptionPane.showMessageDialog(this, "Recommencez svp. Le coût par candidat fourni est incorrect !");
            } else {
                Groupe groupe = metier.findGroupeD1NiveauEtD1Type(niveauSelected, typeSelected);
                anneeSelected = Integer.parseInt(anneeString);
                for (Session s : sessions) {
                    if (s.getAnnee() == anneeSelected) {
                        sessionSelected = s;
                        break;
                    }
                }
                for (Souscentre souscentre : souscentres) {
                    if (souscentre.getNomsouscent().equals(nomSouscentreSelected)) {
                        souscentreSelected = souscentre;
                        break;
                    }
                }
                try {
                    metier.creerMatoeuvresf(new Matoeuvresf(souscentreSelected, sessionSelected, groupe, coutesfSaisi));
                    JOptionPane.showMessageDialog(this, "Coût par candidat ESF enregistré avec succès !");
                } catch (ObcbudgetException ex) {
                    JOptionPane.showMessageDialog(this, "Impossible de d'enregistrer le coût par candidat ESF au sous-centre [" + nomSouscentreSelected + "]" + "\n raison:" + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButtonCreerCoutESFActionPerformed

