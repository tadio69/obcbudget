    private void determinerCoutD1Mopratiquesti(Mopratiquesti mopratiquesti, Map<Souscentre, ArrayList<Statistique>> souscentreStatistiqueMap) {
        specialitePourAffichage = mopratiquesti.getOption().getSigle();
        for (Statistique stat : souscentreStatistiqueMap.get(mopratiquesti.getSouscentre())) {
            if (stat.getOption().getSigle().equals(specialitePourAffichage)) {
                statOption = stat.getNombre();
                break;
            }
        }
        matoeuvre = statOption * mopratiquesti.getCoutcand() + mopratiquesti.getNbpostes() * mopratiquesti.getCoutposte();
        forfait = mopratiquesti.getForfait();
        amortis = statOption * mopratiquesti.getCoutamort();
        totalMopratiquesti = matoeuvre + amortis + forfait;
    }

