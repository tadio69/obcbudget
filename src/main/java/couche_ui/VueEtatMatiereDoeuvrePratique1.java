    private void jButtonListerCoutESFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListerCoutESFActionPerformed
        infoSaisies();
        if (groupedexamensEnstring.equals("") || anneeString.equals("---")) {
            JOptionPane.showMessageDialog(this, "Recommencez svp. Vous devez choisir un groupe d'examens et une session !");
        } else {
            anneeSelected = Integer.parseInt(anneeString);
            matoeuvresfs = metier.findAllMatoeuvresfsD1GroupedexamensD1Session(anneeSelected, niveauSelected, typeSelected);

            if (!matoeuvresfs.isEmpty()) {
                try {
                    List<Map<String, Object>> datasource = new ArrayList<Map<String, Object>>();
                    for (Region region : regions) {
                        int i = 1;
                        for (Matoeuvresf matoeuvresf : matoeuvresfs) {
                            if (matoeuvresf.getSouscentre().getEtablissement().getArrondissement().getDepartement().getRegion().getNomreg().equals(region.getNomreg())) {
                                Map<String, Object> row = new HashMap<>();
                                row.put("session", anneeSelected);
                                row.put("examen", groupedexamensEnstring);
                                row.put("region", region.getNomreg());
                                row.put("num", i++);
                                row.put("souscentre", matoeuvresf.getSouscentre().getNomsouscent());
                                row.put("coutesf", matoeuvresf.getCoutesf());
                                datasource.add(row);
                            }
                        }
                    }
                    JRDataSource jRDataSource = new JRBeanCollectionDataSource(datasource);
                    JasperReport jasperReport = JasperCompileManager.compileReport("src\\main\\java\\reports\\etatListeCoutMatoeuvresfReport.jrxml");
                    JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, jRDataSource);
                    JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);//false empêche jasperviewer de fermer l'appli lorsqu'on le ferme
                    jasperViewer.setVisible(true);
                } catch (JRException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Aucun coût par candidat, par poste, forfait sous-centre et coût d'amortissement trouvé dans la base de données !");
            }
        }
    }//GEN-LAST:event_jButtonListerCoutESFActionPerformed

